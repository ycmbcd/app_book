写人写事的作文题目篇1

    　　每个人都喜欢老师所上的其中一门课，有些人喜欢上体育课，有些人喜欢上美术课，也有些人喜欢上……而我也不例外，每个人喜欢的课程都有喜欢的原因，也是性格所具备的。我喜欢上数学课，我的数学好。但是，我却被一堂别开生面的英语课所陶醉，这堂英语课，让我尝到了上外语课的乐趣与老师那辛勤的泪与汗。

    　　记得那是星期五的晚上，我吃好晚饭，准备去上英语补习班，一进教室我惊呆了，因为我们补习班来了一位不寻常的外国客人,一问才知道，原来他是我们新外教。

    　　上课了，外教和我们一个一个的握了手，我们也进行了自我介绍。后来外教把我们分成了两组第一组的队名叫“icecream”，第二组叫“Frenchfries”，分完队后，我们开始了今天的游戏，老师首先给了我们发了的几幅图片和几个单词，接着老师让我们思考几分钟，这时我看见老师在黑板上画了一个吊死鬼的图像，下面还画了七条横线，这时我已经大概知道老师的意思了，老师是让我们读句子，正确后，让我们猜出一个字母，等七个空格填完后，就组成了一个单词，这样多有趣呀!激烈的战争开始了，其实这不是两队之间的比赛，而是两位队长之间的决赛，其他人则是若无其事的坐在一旁，但还是有同学精神抖擞的喊着加油。

    　　时间过得飞快，一转眼开下课，在这节课里我不光学到了知识，还学到了：我们不能仅针对爱好一门课程，而是要认真学好每一门课。这节英语课是别开生面的，更让我懂得了怎样学习的道理。

